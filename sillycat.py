def help_menu() -> None:
    for (KEY , VALUE) in COMMANDS.items():
        print(f"\t [+] {KEY} -- {VALUE}")



# Main CLI
def cmd_line():
    
    # visuals
    clear()
    logo()


    while True:
        cmd = input("silly > ").lower()

        if cmd == "exit":
            print(f"Have Great Hacking Day...")
            break


        elif cmd == "help":
            help_menu()

        elif cmd == "list":
            list_files()
        
        elif cmd  == "clear":
            clear()

        elif cmd == "load":
            choose_and_run()


# The 'load' Command Logic   ( NOTE : Sorry i put it as the last , im still a noob at neovim and idk how to cut and past , thanks)      
def choose_and_run():
    
    try:
    
        choice = int(input("[file index] :: "))

        choosen_file = TOOLS[choice]

        if choosen_file.endswith(".py"):
            os.system(f"{RUNNERS[0]} scripts/{choosen_file}")
        
        else: print(f"[Error] :: Cant Find File '{choosen_file}'")

    
    except Exception as exp:
        print(f"[Error] :: {exp}")



if __name__ == "__main__":
    cmd_line()
